            {
                Career -= 1;
            }
            else if (AI.Defensing==false && AI.Priority>0)
            {
                AI.Win=false;
            }
            else if (AI.Priority==0)
            {
                if (AI.Thiefing==true)
                {
                    Debug.Log("Continue");
                }
                else
                {
                    AI.Energy -= 1;
                    Energy += 1;
                    Career += 1;
                    Debug.Log("Continue");
                }

            }
            Thiefing=false;
        }
        else
        {
             if (Priority < AI.Priority && AI.Rebounding==false && AI.Defensing==false)  //AI攻击优先级更高
            {
                Win = false;
            }
            else if (Priority < AI.Priority && AI.Rebounding==true && Defensing==false && Priority!=0) //AI反弹成功
            {
                Win = false;
            }
            else if(Priority==AI.Priority)  //优先级一样，相互抵消
            {
                if (AI.Thiefing==true)
                {
                    Energy -= 1;
                    AI.Energy += 1;
                    AI.Career += 1;
                    Debug.Log("Continue");
                }
                else
                {
                    Debug.Log("Continue");
                }

            }
            else if (Defensing==true && AI.Priority!=2)  //玩家防御，AI不用大招，继续游戏
            {
                if (AI.Thiefing==true)
                {
                    AI.Career -= 1;
                    Debug.Log("Continue");
                }
                else
                {
                    Debug.Log("Continue");
                }

            }
            else if(Rebounding==true && (AI.Priority==0 || (AI.Priority!=0 && AI.Defensing==true)))  //玩家反弹失败
            {
                if (AI.Thiefing==true)
                {
                    AI.Career -= 1;
                    Debug.Log("Continue");
                }
                else
                {
                    Debug.Log("Continue");
                }

            }
            else if (Priority==0 && (AI.Defensing==true || AI.Priority==0 || AI.Rebounding==true))  //玩家搓能量，AI不攻击
            {
                if (AI.Thiefing==true)
                {
                    AI.Career += 1;
                    AI.Energy += 1;
                }
                else
                {
                    Debug.Log("Continue");
                }

            }
            else  //否则就是玩家赢
            {
                if (AI.Thiefing==true)
                {
                    Win = false;
                }
                else
                {
                    AI.Win = false;
                }

            }           
        }

        if (ArroganceNumber>=3 && AI.ArroganceNumber>=3)
        {
            ArroganceNumber = 0;
            AI.ArroganceNumber = 0;
            Debug.Log("Continue");
        }
        else if (ArroganceNumber>=3)
        {
            Destroy(AIgameobject);
            Debug.Log("WIN");
        }
        else if(AI.ArroganceNumber>=3) 
        {
            Destroy(gameObject);
            Debug.Log("LOSE");
        }
    }

    public void Sum()  //结算
    {
        if (AI.Win == false)
        {
            if (Thiefing)
            {
                if (AI.Priority == 1)
                {
                    AI.health -= 1;
                }
                else if (AI.Priority == 2)
                {
                    AI.health -= 2;
                }
            }
            else if (Rebounding==true)
            {
                if (AI.Priority==1)
                {
                    AI.health -= 1;
                }
                else if(AI.Priority==2)
                {
                    AI.health -= 2;
                }
            }
            else
            {
                if (Priority == 1)
                {
                    AI.health -= 1;
                }
                else if (Priority == 2)
                {
                    AI.health -= 2;
                }
            }
        }
        else if (Win == false)
        {
            if (AI.Thiefing)
            {
                if (Priority == 1)
                {
                    health -= 1;
                }
                else if (Priority == 2)
                {
                    health -= 2;
                }
            }
            else if (AI.Rebounding == true)
            {
                if (Priority == 1)
                {
                    health -= 1;
                }
                else if (Priority == 2)
                {
                    health -= 2;
                }
            }
            else
            {
                if (AI.Priority == 1)
                {
                    health -= 1;
                }
                else if (AI.Priority == 2)
                {
                    health -= 2;
                }
            }
        }
        else
        {
            Debug.Log("Continue");
        }

        if (health<=0)
        {
            Destroy(gameObject);
            Debug.Log("LOST");
            //时间暂停
        }
        if (AI.health<=0)
        {
            Destroy(AIgameobject);
            Debug.Log(Win);
            //时间暂停
        }

        Chose = false;  //初始化
        Priority = 0;
        Defensing = false;
        Rebounding = false;
        Thiefing = false;
        AI.Rebounding = false;
        AI.Priority = 0;
        AI.Defensing = false;
        AI.Thiefing = false;

        //AI职业点数判定
        if (AI.StringCareer == "Assassin" || AI.StringCareer == "Thief")   //刺客,盗贼职业点数判定
        {
            if (Ground % 2 == 1)
            {
                AI.Career += 1;
            }
        }
        else if (AI.StringCareer == "King")  //国王职业点数判定
        {
            if (AI.Energy % 2 == 0)
            {
                AI.Career = AI.Energy / 2;
            }
            else
            {
                AI.Career = (AI.Energy - 1) / 2;
            }
        }
        else if (AI.StringCareer == "Guard" || AI.StringCareer == "Rascally" || AI.StringCareer == "Arrogance")  //卫士等职业点数判定
        {
            AI.Career = 1;
        }
        else if (AI.StringCareer == "Turtle")  //乌龟职业点数判定
        {
            AI.Career = Energy;
        }


        if (StringCareer == "Assassin" || StringCareer == "Thief")   //刺客,盗贼职业点数判定
        {
            if (Ground % 2 == 1)
            {
                Career += 1;
            }
        }
        else if (StringCareer == "King")  //国王职业点数判定
        {
            if (Energy % 2 == 0)
            {
                Career = Energy / 2;
            }
            else
            {
                Career = (Energy - 1) / 2;
            }
        }
        else if (StringCareer == "Guard" || StringCareer == "Rascally" || StringCareer == "Arrogance")  //卫士等职业点数判定
        {
            Career = 1;
        }
        else if (StringCareer == "Turtle")  //乌龟职业点数判定
        {
            Career = Energy;
        }
    }
