        else
        {
            AI.AIplaying();
            if (Chose==false)
            {
                if (StringCareer== "Pangolin" || StringCareer=="Thief")
                {
                    Defense();
                }
                else
                {
                    RubbingEnergy();
                }
            }
            Despare();
            Sum();
            countDownTimer = 10f;
            Debug.Log(health);
            Debug.Log(AI.health);
        }
